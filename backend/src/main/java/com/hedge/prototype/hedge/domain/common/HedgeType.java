package com.hedge.prototype.hedge.domain.common;

/**
 * 위험회피관계 유형.
 *
 * <p>K-IFRS 1109호 6.5.2에 따른 위험회피관계 3종류 중 본 시스템이 지원하는 2종류입니다.
 * 해외사업장순투자 위험회피는 PoC 범위 외로 제외됩니다.
 *
 * @see K-IFRS 1109호 6.5.2 (위험회피관계 3종류)
 * @see K-IFRS 1109호 6.5.8 (공정가치위험회피 회계처리)
 * @see K-IFRS 1109호 6.5.11 (현금흐름위험회피 OCI 처리)
 */
public enum HedgeType {

    /**
     * 공정가치 위험회피.
     *
     * <p>위험회피수단 및 위험회피대상항목의 공정가치 변동을 모두 당기손익으로 인식합니다.
     *
     * @see K-IFRS 1109호 6.5.8 (공정가치위험회피 회계처리)
     */
    FAIR_VALUE,

    /**
     * 현금흐름 위험회피.
     *
     * <p>유효한 위험회피 부분은 기타포괄손익(OCI)으로 인식하고,
     * 비효과적 부분은 즉시 당기손익으로 인식합니다.
     *
     * @see K-IFRS 1109호 6.5.11 (현금흐름위험회피 OCI 처리)
     * @see K-IFRS 1109호 6.5.12 (비효과적 부분 당기손익 인식)
     */
    CASH_FLOW
}
