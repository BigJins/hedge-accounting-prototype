package com.hedge.prototype.hedge.domain.common;

/**
 * 위험회피수단 유형 열거형.
 *
 * <p>K-IFRS 1109호 6.2.1에 따라 파생상품 전체를 위험회피수단으로 지정할 수 있습니다.
 * 본 PoC는 금융기관 헤지회계에서 가장 빈번하게 사용되는 세 가지 파생상품을 지원합니다.
 *
 * @see K-IFRS 1109호 6.2.1 (위험회피수단 적격성 — 파생상품)
 * @see K-IFRS 1109호 6.2.3 (위험회피수단으로 지정 가능한 항목)
 */
public enum InstrumentType {

    /**
     * 통화선도 (FX Forward).
     *
     * <p>특정 미래 시점에 약정 환율로 외화를 매매하는 파생상품입니다.
     * USD/KRW 환율 위험 헤지에 주로 사용됩니다.
     * 현금흐름 위험회피(CFH) 및 공정가치 위험회피(FVH) 모두 적용 가능합니다.
     *
     * @see K-IFRS 1109호 6.5.8 (공정가치 위험회피 — FX Forward)
     * @see K-IFRS 1109호 6.5.11 (현금흐름 위험회피 — FX Forward)
     */
    FX_FORWARD("통화선도"),

    /**
     * 이자율스왑 (IRS, Interest Rate Swap).
     *
     * <p>고정금리와 변동금리를 교환하는 파생상품입니다.
     * 원화 고정금리채권의 공정가치 위험회피(FVH) 또는
     * 변동금리 부채의 현금흐름 위험회피(CFH)에 사용됩니다.
     *
     * @see K-IFRS 1109호 6.5.8 (공정가치 위험회피 — IRS)
     * @see K-IFRS 1109호 6.5.11 (현금흐름 위험회피 — IRS)
     */
    IRS("금리스왑"),

    /**
     * 통화스왑 (CRS, Cross Currency Swap).
     *
     * <p>서로 다른 통화의 원금과 이자를 교환하는 파생상품입니다.
     * 외화차입금의 환율 및 금리 위험을 동시에 헤지할 수 있습니다.
     * 주로 현금흐름 위험회피(CFH)에 활용됩니다.
     *
     * @see K-IFRS 1109호 6.5.11 (현금흐름 위험회피 — CRS)
     * @see K-IFRS 1109호 B6.4.9 (통화스왑의 헤지비율 산정)
     */
    CRS("통화스왑");

    // -----------------------------------------------------------------------
    // 필드
    // -----------------------------------------------------------------------

    /** 한국어 명칭 */
    private final String koreanName;

    // -----------------------------------------------------------------------
    // 생성자
    // -----------------------------------------------------------------------

    InstrumentType(String koreanName) {
        this.koreanName = koreanName;
    }

    // -----------------------------------------------------------------------
    // 공개 메서드
    // -----------------------------------------------------------------------

    /**
     * 한국어 명칭 반환.
     *
     * @return 한국어 명칭
     */
    public String getKoreanName() {
        return koreanName;
    }
}
