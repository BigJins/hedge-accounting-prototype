package com.hedge.prototype.effectiveness.domain;

/**
 * 유효성 테스트 판정 결과.
 *
 * <p>K-IFRS 1109호는 구 1039호의 80~125% 정량 기준을 폐지하였습니다 (BC6.234).
 * 유효성 판정은 경제적 관계 존재 여부를 중심으로 정성적·정량적 방법으로 평가합니다.
 * Dollar-offset 비율은 보조 참고 지표로만 사용되며, 이진(PASS/FAIL) 단독 판정에
 * 사용해서는 안 됩니다 (B6.4.12~B6.4.13).
 *
 * @see K-IFRS 1109호 BC6.234  (80~125% 정량 기준 폐지)
 * @see K-IFRS 1109호 B6.4.12  (유효성 평가 방법 — 정성적/정량적 병용)
 * @see K-IFRS 1109호 B6.4.13  (Dollar-offset 방법은 선택 가능 방법의 하나)
 * @see K-IFRS 1109호 6.5.6    (유효성 상실 시 전진 중단)
 */
public enum EffectivenessTestResult {

    /**
     * 유효성 확인 — 경제적 관계 성립, 헤지 효과가 충분히 유효함.
     *
     * <p>K-IFRS 1109호 B6.4.12~B6.4.13에 따라 정성적 또는 정량적 방법으로
     * 경제적 관계가 확인되고, Dollar-offset 참고 비율이 반대방향을 나타내는 경우.
     *
     * @see K-IFRS 1109호 B6.4.12 (유효성 평가 방법)
     * @see K-IFRS 1109호 BC6.234 (80~125% 정량 기준 폐지)
     */
    PASS,

    /**
     * 유효성 상실 — 경제적 관계 훼손, 헤지 구조 자체에 문제가 있음.
     *
     * <p>헤지수단과 피헤지항목이 동방향으로 움직이거나(양수 비율),
     * 경제적 관계가 더 이상 성립하지 않는 경우.
     * FAIL 시 헤지 중단(6.5.6) 조치가 필요합니다.
     * 단순 헤지비율 이탈의 경우 FAIL이 아닌 WARNING을 사용합니다.
     *
     * @see K-IFRS 1109호 6.5.6 (위험회피관계 중단 — 조건 미충족 시)
     */
    FAIL,

    /**
     * 경고 — 경제적 관계는 성립하나 헤지비율 조정이 필요한 상태.
     *
     * <p>Dollar-offset 비율이 반대방향(-) 이나 80%~125% 참고 범위를 벗어난 경우.
     * K-IFRS 1109호 BC6.234에 따라 범위 이탈 자체가 FAIL 사유가 되지는 않으며,
     * 위험관리 목적이 유지되는 한 재조정(Rebalancing, 6.5.5)이 우선 검토됩니다.
     *
     * @see K-IFRS 1109호 6.5.5  (헤지비율 재조정 — Rebalancing 의무)
     * @see K-IFRS 1109호 BC6.234 (80~125% 정량 기준 폐지)
     * @see K-IFRS 1109호 B6.4.12 (Dollar-offset 방법은 참고 지표)
     */
    WARNING
}
