package com.hedge.prototype.effectiveness.domain;

/**
 * 유효성 테스트 결과에 따른 필요 조치 유형.
 *
 * <p>K-IFRS 1109호 6.5.5 재조정과 6.5.6 중단 요건에 따라
 * 유효성 테스트 FAIL 시 취해야 할 조치를 나타냅니다.
 *
 * @see K-IFRS 1109호 6.5.5 (헤지비율 재조정 — Rebalancing)
 * @see K-IFRS 1109호 6.5.6 (위험회피관계 중단 — 적용조건 미충족 시)
 * @see K-IFRS 1109호 B6.5.8 (재조정 방법)
 */
public enum ActionRequired {

    /**
     * 조치 불필요 — PASS 판정.
     *
     * <p>유효성 테스트를 통과하여 위험회피관계를 현행대로 유지합니다.
     */
    NONE,

    /**
     * 헤지비율 재조정 필요.
     *
     * <p>유효성 테스트 FAIL이나 위험관리 목적이 유지되는 경우,
     * 헤지비율을 조정하여 유효성을 회복합니다.
     * 재조정 전 비효과성을 먼저 인식해야 합니다.
     *
     * @see K-IFRS 1109호 6.5.5 (재조정 의무)
     * @see K-IFRS 1109호 B6.5.8 (재조정 시 비효과성 먼저 인식)
     */
    REBALANCE,

    /**
     * 위험회피관계 중단 필요.
     *
     * <p>위험관리 목적이 변경되었거나 위험회피관계 적용 조건이
     * 더 이상 충족되지 않아 헤지회계 적용을 전진 중단합니다.
     * 자발적 중단은 K-IFRS 1109호 6.5.6에 따라 금지됩니다.
     *
     * @see K-IFRS 1109호 6.5.6 (자발적 취소 불가 — 조건 미충족 시만 중단)
     * @see K-IFRS 1109호 B6.5.26 (중단 후 OCI 잔액 처리)
     */
    DISCONTINUE
}
